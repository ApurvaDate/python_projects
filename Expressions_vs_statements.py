#Expressions vs statements

iq = 100

user_age = iq/5

#here expression is iq/5 
#so expression is piece of code that produces a value.

#a statement is entire line of code that performs some action.
#here user_age = iq/5 is the statement in which we are dividing iq by 5 and storing it in a variable called user_age


